/**
 * Script để cập nhật image URLs từ local path sang CDN URL
 * Chạy: node scripts/update-cdn-urls.mjs
 */

import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.resolve(__dirname, '..')

// Cấu hình CDN URL - THAY ĐỔI THEO CDN CỦA BẠN
const CDN_BASE_URL = process.env.CDN_BASE_URL || 'dsv0bl2xjh4mi.cloudfront.net'

// Pattern để replace
const LOCAL_PREFIX = '/images-opt/'
const CDN_PREFIX = `${CDN_BASE_URL}/images-opt/`

function processProjectData(data) {
  let updated = 0

  // Process all slides in the project
  if (data.slides && Array.isArray(data.slides)) {
    for (const slide of data.slides) {
      // Update main image
      if (slide.image && slide.image.startsWith(LOCAL_PREFIX)) {
        slide.image = slide.image.replace(LOCAL_PREFIX, CDN_PREFIX)
        updated++
      }

      // Update original
      if (slide.original && slide.original.startsWith('/images/')) {
        slide.original = slide.original.replace('/images/', `${CDN_BASE_URL}/images/`)
        updated++
      }

      // Update webp variants
      if (slide.webp && Array.isArray(slide.webp)) {
        for (const variant of slide.webp) {
          if (variant.url && variant.url.startsWith(LOCAL_PREFIX)) {
            variant.url = variant.url.replace(LOCAL_PREFIX, CDN_PREFIX)
            updated++
          }
        }
      }

      // Update jpeg variants
      if (slide.jpeg && Array.isArray(slide.jpeg)) {
        for (const variant of slide.jpeg) {
          if (variant.url && variant.url.startsWith(LOCAL_PREFIX)) {
            variant.url = variant.url.replace(LOCAL_PREFIX, CDN_PREFIX)
            updated++
          }
        }
      }

      // Update blurDataURL
      if (slide.blurDataURL && slide.blurDataURL.startsWith(LOCAL_PREFIX)) {
        slide.blurDataURL = slide.blurDataURL.replace(LOCAL_PREFIX, CDN_PREFIX)
        updated++
      }
    }
  }

  return updated
}

function main() {
  const filePath = path.join(ROOT, 'src', 'data', 'projects.generated.ts')

  console.log('🔄 Đang đọc file projects.generated.ts...')
  let content = fs.readFileSync(filePath, 'utf-8')

  // Parse JSON content from TypeScript export
  const jsonMatch = content.match(/export const projects: Project\[\] = ([\s\S]*)/)
  if (!jsonMatch) {
    console.error('❌ Không tìm thấy dữ liệu projects trong file')
    process.exit(1)
  }

  // Extract and parse JSON
  let jsonStr = jsonMatch[1]
  const data = JSON.parse(jsonStr)

  let totalUpdated = 0
  for (const project of data) {
    totalUpdated += processProjectData(project)
  }

  // Update content with new JSON
  const newJsonStr = JSON.stringify(data, null, 2)
  content = `/* eslint-disable */\n// Auto-generated by scripts/update-cdn-urls.mjs — updated for CDN\n// CDN Base URL: ${CDN_BASE_URL}\nimport type { Project } from './projects.types'\n\nexport const projects: Project[] = ${newJsonStr}\n`

  fs.writeFileSync(filePath, content, 'utf-8')

  console.log(`✅ Đã cập nhật ${totalUpdated} URLs sang CDN: ${CDN_BASE_URL}`)
  console.log(`📁 File: ${filePath}`)
}

main()
